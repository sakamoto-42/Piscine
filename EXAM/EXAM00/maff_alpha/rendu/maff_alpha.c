#include <unistd.h>

int	main(void)
{
	int		i;
	char	c;

	i = 1;
	while (i <= 26)
	{
		if (i % 2 == 1)
			c = 97 + i - 1;
		else
			c = 65 + i - 1;
		write(1, &c, 1);
		i++;
	}
	write(1, "\n", 1);
	return (0);
}
