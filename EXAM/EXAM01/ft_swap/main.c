#include <unistd.h>
#include "ft_swap.h"

int	main(void)
{
	int		i;
	int		j;
	char	c;

	i = 5;
	j = 9;
	c = i + '0';
	write(1, &c, 1);
	write(1, "\n", 1);
	c = j + '0';
	write(1, &c, 1);
	write(1, "\n", 1);
	ft_swap(&i, &j);
	c = i + '0';
	write(1, &c, 1);
	write(1, "\n", 1);
	c = j + '0';
	write(1, &c, 1);
	write(1, "\n", 1);
	return (0);
}
