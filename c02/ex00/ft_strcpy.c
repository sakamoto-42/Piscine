char	*ft_strcpy(char *dest, char *src)
{
	int	i;
	int	size;

	i = 0;
	while (src[i] != '\0')
		i++;
	size = i;
	i = 0;
	while (i <= size)
	{
		dest[i] = src[i];
		i++;
	}
	return (dest);
}
